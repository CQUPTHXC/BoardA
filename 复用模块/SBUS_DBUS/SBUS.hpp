#ifndef SBUS_HPP
#define SBUS_HPP
#include <Arduino.h>

class SBUS{
public:
    SBUS(uint8_t pin,HardwareSerial* serial){
        _pin = pin;
        _serial = serial;
    };
    SBUS(uint8_t pin){
        _pin = pin;
        _serial = &Serial2;
    };
    SBUS(){
        _serial = &Serial2;
        _pin=6;
    };
    void setup(){
        pinMode(_pin, INPUT);
        _serial->begin(100000,SERIAL_8E2,_pin,-1);
        memset(raw_data,0,sizeof(raw_data));
        memset(channel_data,0,sizeof(channel_data));
        flag = 0;
        xTaskCreate(loop_task,"loop_task",1024,this,2,NULL);
    };
    static void loop_task(void* p){
        SBUS* obj = (SBUS*)p;
        while(true){
            delay(1);

            if(obj->_serial->available()<25){
                continue;
            }

            while(obj->_serial->peek() != 0xf){
                obj->_serial->read();
                delay(1);
            }
            obj->_serial->readBytes(obj->raw_data,25);

            if(obj->raw_data[24] != 0x00){
                continue;
            }

            //每11位为一个通道
            obj->channel_data[ 0] = ((int16_t)obj->raw_data[ 1] >> 0 | ((int16_t)obj->raw_data[ 2] << 8 )) & 0x07FF;
            obj->channel_data[ 1] = ((int16_t)obj->raw_data[ 2] >> 3 | ((int16_t)obj->raw_data[ 3] << 5 )) & 0x07FF;
            obj->channel_data[ 2] = ((int16_t)obj->raw_data[ 3] >> 6 | ((int16_t)obj->raw_data[ 4] << 2 )  | (int16_t)obj->raw_data[ 5] << 10 ) & 0x07FF;
            obj->channel_data[ 3] = ((int16_t)obj->raw_data[ 5] >> 1 | ((int16_t)obj->raw_data[ 6] << 7 )) & 0x07FF;
            obj->channel_data[ 4] = ((int16_t)obj->raw_data[ 6] >> 4 | ((int16_t)obj->raw_data[ 7] << 4 )) & 0x07FF;
            obj->channel_data[ 5] = ((int16_t)obj->raw_data[ 7] >> 7 | ((int16_t)obj->raw_data[ 8] << 1 )  | (int16_t)obj->raw_data[ 9] <<  9 ) & 0x07FF;
            obj->channel_data[ 6] = ((int16_t)obj->raw_data[ 9] >> 2 | ((int16_t)obj->raw_data[10] << 6 )) & 0x07FF;
            obj->channel_data[ 7] = ((int16_t)obj->raw_data[10] >> 5 | ((int16_t)obj->raw_data[11] << 3 )) & 0x07FF;
            obj->channel_data[ 8] = ((int16_t)obj->raw_data[12] >> 0 | ((int16_t)obj->raw_data[13] << 8 )) & 0x07FF;
            obj->channel_data[ 9] = ((int16_t)obj->raw_data[13] >> 3 | ((int16_t)obj->raw_data[14] << 5 )) & 0x07FF;
            obj->channel_data[10] = ((int16_t)obj->raw_data[14] >> 6 | ((int16_t)obj->raw_data[15] << 2 )  | (int16_t)obj->raw_data[16] << 10 ) & 0x07FF;
            obj->channel_data[11] = ((int16_t)obj->raw_data[16] >> 1 | ((int16_t)obj->raw_data[17] << 7 )) & 0x07FF;
            obj->channel_data[12] = ((int16_t)obj->raw_data[17] >> 4 | ((int16_t)obj->raw_data[18] << 4 )) & 0x07FF;
            obj->channel_data[13] = ((int16_t)obj->raw_data[18] >> 7 | ((int16_t)obj->raw_data[19] << 1 )  | (int16_t)obj->raw_data[20] <<  9 ) & 0x07FF;
            obj->channel_data[14] = ((int16_t)obj->raw_data[20] >> 2 | ((int16_t)obj->raw_data[21] << 6 )) & 0x07FF;
            obj->channel_data[15] = ((int16_t)obj->raw_data[21] >> 5 | ((int16_t)obj->raw_data[22] << 3 )) & 0x07FF;
            obj->flag = obj->raw_data[24];

        }
    }
    uint16_t operator[] (uint8_t i){
        return channel_data[i-1];
    }
    uint16_t get_channel_data(uint8_t i){
        return channel_data[i-1];
    }
    uint8_t get_flag(){
        return flag;
    }
protected:
    uint8_t _pin;
    HardwareSerial* _serial;
    uint8_t raw_data[25];
    uint16_t channel_data[16];
    uint8_t flag;
};

#endif
